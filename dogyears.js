// Creating the variable in human years
const myAge = 24;
console.log(`My age in human years is ${myAge}`)

// Creating a variable and instantiating to 2
let earlyYears = 2;

// Multiply 10.5 to the earlyYears variable
earlyYears *= 10.5;

// Subtracting the early years of the dog from the human years
let laterYears = myAge - earlyYears;

// Multiply the remaining years of the dog by 4
laterYears *= 4;

console.log(`Later age in dog years is ${laterYears}`);

// Adding the early years and th later years
let myAgeInDogYears = earlyYears + laterYears;

// Declare a variable with your name in lower case letters
const myName = 'Leonard'.toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);